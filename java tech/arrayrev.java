class arrayrev{
    
    public static void main(String args[]){
        int[] a={10,3,4,5,-9,6};
        int rev=a[0];
        for(int i=0;i<=a.length-1;i++)
            {
               
            rev=rev(a[i]);
            System.out.println("reversed array is: "+rev);


            }
       


}
    
}
